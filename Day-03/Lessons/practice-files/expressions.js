// Simple Expressions
let num = 5 + 10;  // Expression evaluates to 15
console.log(num);

// Conditional Expression
let age = 22;
let status = (age >= 18) ? "Adult" : "Minor";
console.log(status); // "Adult"

// Complex Expression
let total = ((8 + 2) * 3) / (10 % 4);
console.log(`Total: ${total}`);
